function solution(board) {
  let answer = 0;
  let n = board.length;
  let dx = [-1, -1, 0, 1, 1, 1, 0, -1];
  let dy = [0, 1, 1, 1, 0, -1, -1, -1];
  let queue = [];

  for (let a = 0; a < n; a++) {
    for (let b = 0; b < n; b++) {
      if (board[a][b] === 1) {
        board[a][b] = 0;
        queue.push([a, b]);
        while (queue.length) {
          let len = queue.length;
          let [x, y] = queue.shift();

          for (let k = 0; k < 8; k++) {
            let nx = x + dx[k];
            let ny = y + dy[k];
            if (nx >= 0 && nx < n && ny >= 0 && ny < n && board[nx][ny] === 1) {
              board[nx][ny] = 0;
              queue.push([nx, ny]);
            }
          }
        }
        answer++;
      }
    }
  }

  return answer;
}

